IngredientCategory categorizeIngredient({
  required String? type,
  required String name,
}) {
  final lowType = type?.toLowerCase().trim();
  final lowName = name.toLowerCase().trim();
  if (type == null) {
    final IngredientCategory nameMatch =
        exceptions[lowName] ?? IngredientCategory.other;
    return nameMatch;
  }
  final IngredientCategory typeMatch =
      typeMap[lowType] ?? IngredientCategory.other;
  return typeMatch;
}
